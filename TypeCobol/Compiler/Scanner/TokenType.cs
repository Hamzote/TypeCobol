        // 460 -> 460 : Operators - TypeCobol
        TypeCobolOperators= 460, 

        // 461 -> 463 : Compiler directives
        CompilerDirective = 461,
        // 464 -> 464 : Internal token groups - used by the preprocessor only
        InternalTokenGroup = 464
    }

